void solve()
{
    int words;
    cin >> words;
    bool ans = true;
    while (words--)
    {
        int lang1 = 0, lang2 = 0, none = 0;

        string s;
        cin >> s;

        for (char &x : s)
        {
            if ('a' <= x and x <= 'm')
                lang1++;
            else if ('N' <= x and x <= 'Z')
                lang2++;
            else
                none++;
        }
        ans &= (none == 0) and ((lang1 == 0) or (lang2 == 0));
    }

    if (ans)
        cout << "YES";
    else
        cout << "NO";

    cout << endl;
}

