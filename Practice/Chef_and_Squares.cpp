int f(int n)
{
    int ans = INT32_MAX;
    for (int i = 1; i * i <= n; i++)
    {
        if (n % i == 0)
        {
            int f1 = i;
            int f2 = n / i;

            if ((f2 - f1) % 2 == 0 and f2 > f1)
                ans = min(ans, (f2 - f1) / 2);
        }
    }
    if (ans == INT32_MAX)
        return -1;
    ans *= ans;
    return ans;
}

